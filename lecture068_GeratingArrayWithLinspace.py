import numpy

print(numpy.arange(5))
print(numpy.arange(1, 100, 2), '\n')


# Esse array vai imprimir
# 50 numeros aleatórios
# entre 2 e 3 por padrão do metodo
print(numpy.linspace(2.0, 3.0), '\n')

# Aqui, dos 50 numeros,
# peço uma amostra de 5
# numeros aleatórios
# uniformemente espaçado
print(numpy.linspace(2.0, 3.0, 5), '\n')


